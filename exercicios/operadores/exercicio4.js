// Declare duas variáveis booleanas e use o operador OR para verificar se pelo menos uma delas é verdadeira. 
// Exiba o resultado no console.

let sueli = true;
let fatima = false;

if (sueli || fatima) {
    console.log(`Uma delas é verdadeira.`);
} else {
    console.log(`As duas são falsas.`);
};
